package testes;

public class Calculadora {
    
    public int adicao (int numA, int numB){
        return numA + numB;
    }

    public int multiplicacao (int numA, int numB){
        return numA * numB;
    }

    public int divisao (int numA, int numB){
        return numA / numB;
    }

    public int subtracao (int numA, int numB){
        return numA - numB;
    }

}
